#include<stdio.h>
int main()
{
	int a = 10;
	long int li = 45000;
	float f = 45.63;
	double d = 45.8965478;
	char ch = 'A';
	
	printf("\nValue of a = %d",a);
	printf("\nValue of li = %ld",li);
	printf("\nValue of f = %.4f",f);
	printf("\nValue of d = %lf",d);
	printf("\nValue of ch = %c",ch);
	return 0;
}
